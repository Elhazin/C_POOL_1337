#include <unistd.h>
int	main(int ac, char **av)
{
	int	a;
	int	b;

	a = 0;
	b = 1;
	if (ac > 1)
	{
		while (b < ac)
		{
			while (av[b][a] != '\0')
			{
				write(1, &av[b][a], 1);
				a++;
			}
			write(1, "\n", 1);
			a = 0;
			b++;
		}
	}
}
